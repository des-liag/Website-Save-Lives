package save_lives_2021_2022;

public class Aed {
    
    private int aed_id;
    private String aed_region;
    private String aed_area;
    private String aed_street;
    private String aed_number;
    private String aed_zip;

    public Aed(int aed_id, String aed_region, String aed_area, String aed_street, String aed_number, String aed_zip) {
        this.aed_id = aed_id;
        this.aed_region = aed_region;
        this.aed_area = aed_area;
        this.aed_street = aed_street;
        this.aed_number = aed_number;
        this.aed_zip = aed_zip;
    }

    public int getAed_id() {
        return aed_id;
    }

    public void setAed_id(int aed_id) {
        this.aed_id = aed_id;
    }

    public String getAed_region() {
        return aed_region;
    }

    public void setAed_region(String aed_region) {
        this.aed_region = aed_region;
    }

    public String getAed_area() {
        return aed_area;
    }

    public void setAed_area(String aed_area) {
        this.aed_area = aed_area;
    }

    public String getAed_street() {
        return aed_street;
    }

    public void setAed_street(String aed_street) {
        this.aed_street = aed_street;
    }

    public String getAed_number() {
        return aed_number;
    }

    public void setAed_number(String aed_number) {
        this.aed_number = aed_number;
    }

    public String getAed_zip() {
        return aed_zip;
    }

    public void setAed_zip(String aed_zip) {
        this.aed_zip = aed_zip;
    }

    

    

}